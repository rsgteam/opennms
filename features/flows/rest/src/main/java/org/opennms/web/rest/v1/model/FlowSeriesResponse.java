package org.opennms.web.rest.v1.model;

import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;

@XmlAccessorType(XmlAccessType.NONE)
public class FlowSeriesResponse {

    @XmlAttribute(name="start")
    private long start;

    @XmlAttribute(name="end")
    private long end;

    @XmlElement(name="labels")
    private List<String> labels;

    @XmlElement(name="timestamps")
    private List<Long> timestamps;

    @XmlElement(name="columns")
    private List<List<Double>> columns;

    public long getStart() {
        return start;
    }

    public void setStart(long start) {
        this.start = start;
    }

    public long getEnd() {
        return end;
    }

    public void setEnd(long end) {
        this.end = end;
    }

    public List<String> getLabels() {
        return labels;
    }

    public void setLabels(List<String> labels) {
        this.labels = labels;
    }

    public List<Long> getTimestamps() {
        return timestamps;
    }

    public void setTimestamps(List<Long> timestamps) {
        this.timestamps = timestamps;
    }

    public List<List<Double>> getColumns() {
        return columns;
    }

    public void setColumns(List<List<Double>> columns) {
        this.columns = columns;
    }
}

package org.opennms.web.rest.v1.model;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;

@XmlAccessorType(XmlAccessType.NONE)
public class FlowRequest {

    @XmlAttribute(name="report")
    private ReportType report;

    @XmlAttribute(name="start")
    private long start;

    @XmlAttribute(name="end")
    private long end;

    @XmlElement(name="filter")
    private FlowRequestFilter filter;

    @XmlElement(name="N")
    private int N;

    public ReportType getReport() {
        return report;
    }

    public void setReport(ReportType report) {
        this.report = report;
    }

    public long getStart() {
        return start;
    }

    public void setStart(long start) {
        this.start = start;
    }

    public long getEnd() {
        return end;
    }

    public void setEnd(long end) {
        this.end = end;
    }

    public FlowRequestFilter getFilter() {
        return filter;
    }

    public void setFilter(FlowRequestFilter filter) {
        this.filter = filter;
    }

    public int getN() {
        return N;
    }

    public void setN(int n) {
        N = n;
    }
}
